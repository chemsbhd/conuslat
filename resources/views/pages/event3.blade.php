            </p>

            <h2>Inscriptions et Pratique</h2>
            <p>
                L'inscription à l'événement est gratuite mais obligatoire. Les étudiants peuvent s'inscrire en ligne sur le site du consulat avant le <strong>10 janvier 2025</strong>. 
                Le forum se tiendra de 9h à 17h à l'adresse suivante : <strong>Consulat du Maroc à Paris, 12 Rue de la Saussaye, 75116 Paris</strong>. Un espace de restauration 
